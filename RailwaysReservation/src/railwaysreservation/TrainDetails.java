    public void TrainDetailList() {
        
        try{
            con=DatabaseConnection.getConnection();
            String query="select * from trainlist";
            stmt=con.createStatement();
            set=stmt.executeQuery(query);

            ResultSetMetaData rsd=set.getMetaData();
            int c=rsd.getColumnCount();
             
            DefaultTableModel model=(DefaultTableModel)listTable.getModel();
            model.setRowCount(0);
            while(set.next()){
                Vector v=new Vector();
                for(int i=0;i<=c;i++){
                    v.add(set.getString("TRAINNO"));
                    v.add(set.getString("TRAINNAME"));
                    v.add(set.getString("TRAINDATE"));
                    v.add(set.getString("SOURCE"));
                    v.add(set.getString("DESTINATION"));
                    v.add(set.getString("TIME"));
                     
                 
                }
                model.addRow(v);
        }
        }catch(Exception e){
            e.printStackTrace();
        }
    }
    
