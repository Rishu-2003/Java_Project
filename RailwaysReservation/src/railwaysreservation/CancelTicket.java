    private void btnCancelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelActionPerformed
       try{
            PNR=txtPNRno.getText();
            name=txtName.getText();
            date=txtDate.getText();
            source=txtFrom.getText();
            destination=txtTo.getText();
            Connection con=DatabaseConnection.getConnection();
            String cancel="DELETE FROM ticketBooking WHERE Name=? and PNRno=?";
            PreparedStatement pstmt=con.prepareStatement(cancel);
            pstmt.setString(1,name);
            pstmt.setString(2,PNR);

            pstmt.executeUpdate();
            JOptionPane.showMessageDialog(rootPane, "Cancel Confirm ", "", INFORMATION_MESSAGE);
    
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "Not Cancel", "", INFORMATION_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnCancelActionPerformed

